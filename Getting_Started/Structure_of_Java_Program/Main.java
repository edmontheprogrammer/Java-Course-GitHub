/* Understanding the structure of a Java
    Program: 

 * main() function; every Java program must have a 
 * main() method. This is the entry point of execution. 
 * 
 * Main class; Every Java program must have class that 
 * contains the 'main()' function. The name of that
 * class is called 'Main'. 
 * 
 * method; a method is a function that defined inside a class or 
 * part of a class. Term method and functions are used in similar context.
 * 
 * access modifers ; In Java, all of the classes and methods should have 
 * access modifers. An access modifier is a special keyword that determines
 * if other classes and methods in this program can access these classes
 * and methods. We have various access modifiers like 'public' and 'private'.
 * Most of the time we use the 'public' access modifer, so we put that in front
 * of our class and method declarations as shown below in this class
 * "public class ..." and "public static ...". 
 * 
 * This is the basic structure of a Java program. 
 * 
 * Naming convention of Java; In Java we use the PascalNamingConvention, 
 * meaning the first letter of every word should be in upper case. 
 * 
 */

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello,  2World");
    }
}