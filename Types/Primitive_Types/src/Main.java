public class Main {
        /*
        * Two Categories of Types in Java: Primitive Type and Reference Type
        *
        * Primitive type is for storing simple values. Primitive types are byte,
        * short, int, long, float, double, char, boolean
        *
        * Reference type is for storing complex objects.
        * */
        public static void main(String[] args) {

            byte age = 30;
            long viewsCount = 3_123_456_789L;
            float price = 10.99F;
            char letter = 'A';
            boolean isEligible = false;

        }

}
